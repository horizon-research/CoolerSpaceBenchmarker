XYZ2RGB = np.linalg.inv(RGB2XYZ)

XYZ2LMS = np.asarray([
    [
        0.210576,
        -0.417076,
        0
    ],
    [
        0.855098,
        1.17726,
        0
    ],
    [
        -0.0396983,
        0.0786283,
        0.516835
    ]
])

LMS2XYZ = np.linalg.inv(XYZ2LMS)


@njit
def mixing(k1: np.ndarray, k2: np.ndarray, s1: np.ndarray, s2: np.ndarray, d1: np.ndarray, d2: np.ndarray, light: np.ndarray):
    d_total = d1 + d2
    k = (k1 * d1 + k2 * d2) / d_total
    s = (s1 * d1 + s2 * d2) / d_total
    ks = k / s
    r = 1 + ks - np.sqrt(ks ** 2 + 2 * ks)

    reflected = r * light

    original_shape = reflected.shape

    reflected_rgb = ((reflected.reshape((-1, 89)) @ Light2LMS) @ LMS2XYZ) @ XYZ2RGB
    reflected_srgb = (reflected_rgb ** 0.4545) * 255

    return reflected_srgb.reshape((original_shape[0], original_shape[1], 3))

